package section01;

public class Save {
    public static void main(String[] args) {
        // 변수에 값을 저장할 때에는 대입 연산자(=)를 사용해야 합니다.
        int age;
        age = 29; // 대입연산자 오른쪽의 값이 대입연산자 왼쪽의 변수에 저장이 됩니다.

        int stature = 175; // 변수는 선언과 동시에 값을 저장할 수 있습니다.

        int x = 3, y = 4; // 마찬가지로 콤마(,)를 활용해 여러 변수를 동시 선언하면서 값을 저장할 수 있습니다.
    }
}
